#include<iostream>
#include<vector>
#include<algorithm>

using namespace std;

typedef pair<int, int> PII;

const int N = 300005;//N = n + 2m

int n,m;
int a[N], s[N];//a存储各坐标上的x，s为a的前缀和数组

vector<int> alls;//存储所有涉及到的坐标
vector<PII> add, query;//add记录x和c，query记录区间边界l和r

//二分查找坐标 x 在alls中的索引
int find(int x){
	int l = 0,r = alls.size() - 1;
	while(l < r){
		int mid = l + r >> 1;
		if(alls[mid] >= x) r = mid;
		else l = mid + 1;
	}
	return r + 1;
}

vector<int>::iterator unique(vector<int> &a){
	//第一个数不重复，所以从第二个数开始
	int j = 1;//下面将保证区间 0~j 内均为不重复的数
	for(int i = 1;i < a.size();i++)
		if(a[i] != a[i - 1]) a[j++] = a[i];
		
	return a.begin() + j;
}

int main(){
	cin >> n >> m;
	for(int i = 0;i < n;i++){
		int x, c;
		cin >> x >> c;
		add.push_back({x, c});
		alls.push_back(x);
	}
	
	for(int i = 0;i < m;i++){
		int l, r;
		cin >> l >> r;
		query.push_back({l, r});
		
		alls.push_back(l);
		alls.push_back(r);
	}
	
	//对所有的坐标去重
	sort(alls.begin(),alls.end());
	alls.erase(unique(alls),alls.end());
	
    //将排序好的各坐标的数按全序存储到a数组中
    for(PII item: add){
    	int x = find(item.first);
    	a[x] += item.second;
	}
	
	//预处理所有位置上的数前缀和
	for(int i = 1;i <= alls.size();i++) s[i] = s[i - 1] + a[i];
	
	//处理询问
	for(PII item: query){
		int l = find(item.first), r = find(item.second);
		cout << s[r] - s[l - 1] << endl;
	}
	return 0;
}
